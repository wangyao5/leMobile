package com.letv.emm.vo;

public class KingdeePushVo {
    private String no;// 工作圈号 102
    private String pub;// 公共号编码
    private String pubk;// 公共号秘钥
    private String appid;// 轻应用ID

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getPub() {
        return pub;
    }

    public void setPub(String pub) {
        this.pub = pub;
    }

    public String getPubk() {
        return pubk;
    }

    public void setPubk(String pubk) {
        this.pubk = pubk;
    }

    public String getAppid() {
        return appid;
    }

    public void setAppid(String appid) {
        this.appid = appid;
    }
}
